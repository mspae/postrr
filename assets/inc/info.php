      <h2>Über postrr</h2>
        <h3>Die Autoren</h3>
        <p>postrr wurde im Herbst 2012 von Mulugheta und Martin als Projekt für den PHP-Blockunterricht konzipiert und programmiert.</p>
        <h3>Backend</h3>
        <p>Die Software ist in PHP und Javascript mit MySQL-Anbindung geschrieben. Es gibt zwei Datenbanktabellen: Users und Posts, respektive für Benutzerkonten und Blogposts. Die Navigation wurde über einen GET-Parameter umgesetzt, je nachdem welcher Parameter angegeben ist, wird die passende PHP-Datei eingefügt.</p>
        
        <p>Die Konfiguration kann in der config.php vorgenommen werden, hier werden sämtliche von der Software verwendeten Strings in Konstanten definiert. Eine Übersetzung der Oberfläche kann so durch Bearbeiten dieser einen Datei vorgenommen werden.</p>
        <h3>Frontend</h3>
        <p>postrr ist html5-konform, die CSS-Anweisungen wurden über LESS generiert. Die Website ist für alle möglichen Bildschirmgrößen optimiert.</p>
